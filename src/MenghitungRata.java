public class MenghitungRata {
    public static void main(String[] args) {
        int nilai1 = 3;  // [2] Memberikan tipe data int pada nilai1
        int nilai2 = 5;  // [3] Memberikan tipe data int pada nilai2
        int nilai3 = 2;  // [4] Memberikan tipe data int pada nilai3

        // Menghitung rata-rata
        double result = (nilai1 + nilai2 + nilai3) / 3.0;  // [5] Memberikan tipe data double pada result

        System.out.println("Hasil Rata-rata adalah : " + result);
    }
}
